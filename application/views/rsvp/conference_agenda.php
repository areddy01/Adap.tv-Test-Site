<h3>How the Future of TV and Video Will Unfold</h3>
<p>
TV buying still operates on a reach and scale basis, while online buying is focused on optimizing campaigns. And while Digital and TV ads are bought and sold differently, strides are being made to bring the two mediums together. Leading agency executives reveal how they see TV and Video unfolding and what it will mean for the industry and for advertisers.
</p>
<p>
Sebastian Robin - COO Affiperf, Havas<br/>
Marco Bertozzi - Executive Managing Director, EMEA, VivaKi
</p>
<p>Moderated by Brian Fitzpatrick, Managing Director EMEA, Adap.tv</p>

<div class="thinHrule"></div>

<h3>Common Language? Common Currency?</h3>
<p>
Measurement could quite possibly be the industry's biggest challenge. Is a common currency for TV and video measurement possible? Hear from agency and publisher executives on how they see traditional television advertising and online video advertising working together. How are they recalibrating the digital transaction in this new age of audience-based buying? 
</p>
<p>
Ken Nugent - Sales and Business Development Manager, RTE Digital
</p>
<p>Moderated by Vijay Rao, SVP, Research & Strategy, Adap.tv</p>


